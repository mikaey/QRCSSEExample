package com.paypal;

public enum DisplayMediumType {
    /** Specifies that the QR code is displayed on a lanyard. */
    PHYSICAL_LANYARD,

    /** Specifies that the QR code is displayed on a sticker. */
    PHYSICAL_STICKER,

    /** Specifies that the QR code is displayed on a card. */
    PHYSICAL_CARD,

    /** Specifies that the QR code is displayed on a digital medium like a mobile screen. */
    DIGITAL
}
